'use client'

import { useMemo, useRef, useState } from 'react'
import Link from 'next/link'
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card'
import { Button } from '@/components/ui/button'
import { Badge } from '@/components/ui/badge'
import { Check, Copy, Lock, TrendingUp } from 'lucide-react'
import { track } from '@/lib/analytics'

export type PreviewLead = {
  id: string
  company: string
  score: number
  fitReason: string
  whyNow: string
  scoreFactors: string[]
  outreachSubject: string | null
  outreachBody: string
  updatedAtLabel: string
}

type LeadResultsPreviewClientProps = {
  company: string
  leads: PreviewLead[]
}

const FREE_PREVIEW_LIMIT = 3

export function LeadResultsPreviewClient({ company, leads }: LeadResultsPreviewClientProps) {
  const [copiedLeadId, setCopiedLeadId] = useState<string | null>(null)
  const [showUpgradeModal, setShowUpgradeModal] = useState(false)
  const [copiedShareLink, setCopiedShareLink] = useState(false)
  const [copiedLeadShareLink, setCopiedLeadShareLink] = useState<string | null>(null)
  const [upgradePromptReason, setUpgradePromptReason] = useState<'results_loaded' | 'copy_action' | 'campaign_action'>('results_loaded')
  const [showInvitePromptLeadId, setShowInvitePromptLeadId] = useState<string | null>(null)
  const activationTrackedRef = useRef(false)
  const visibleLeads = useMemo(() => leads.slice(0, FREE_PREVIEW_LIMIT), [leads])
  const hiddenLeadCount = Math.max(0, leads.length - FREE_PREVIEW_LIMIT)

  const signupRedirect = useMemo(() => {
    const redirect = `/lead-results?company=${encodeURIComponent(company)}`
    return `/signup?redirect=${encodeURIComponent(redirect)}`
  }, [company])

  const referralLink = useMemo(() => {
    const params = new URLSearchParams({
      redirect: `/lead-results?company=${encodeURIComponent(company)}`,
      ref: 'preview-share',
    })
    return `/signup?${params.toString()}`
  }, [company])

  function trackActivation(trigger: 'copy_message' | 'add_to_campaign'): void {
    if (activationTrackedRef.current) return
    activationTrackedRef.current = true
    track('activation_completed', { source: 'lead_results_preview', trigger })
  }

  async function copyOutreach(lead: PreviewLead): Promise<void> {
    const message = [
      lead.outreachSubject ? `Subject: ${lead.outreachSubject}` : null,
      lead.outreachBody,
      '',
      'Generated with RaelInfo',
      'https://raelinfo.com',
    ]
      .filter((line): line is string => Boolean(line))
      .join('\n\n')
    try {
      await navigator.clipboard.writeText(message)
      setCopiedLeadId(lead.id)
      setUpgradePromptReason('copy_action')
      setShowUpgradeModal(true)
      trackActivation('copy_message')
      setTimeout(() => setCopiedLeadId((curr) => (curr === lead.id ? null : curr)), 2000)
      track('demo_preview_outreach_copied', { source: 'lead_results_preview', leadId: lead.id })
    } catch {
      setCopiedLeadId(null)
    }
  }

  async function copyShareLink(): Promise<void> {
    const link = `${window.location.origin}/lead-results?company=${encodeURIComponent(company)}`
    try {
      await navigator.clipboard.writeText(link)
      setCopiedShareLink(true)
      setTimeout(() => setCopiedShareLink(false), 1600)
      track('lead_results_share_link_copied', { source: 'lead_results_preview', companyLen: company.length })
    } catch {
      setCopiedShareLink(false)
    }
  }

  async function copyLeadShareLink(lead: PreviewLead): Promise<void> {
    const base = typeof window !== 'undefined' ? window.location.origin : 'https://raelinfo.com'
    const shareUrl = `${base}/public/lead-preview/${encodeURIComponent(lead.id)}`
    try {
      await navigator.clipboard.writeText(shareUrl)
      setCopiedLeadShareLink(lead.id)
      setTimeout(() => setCopiedLeadShareLink((curr) => (curr === lead.id ? null : curr)), 1600)
      track('lead_preview_share_link_copied', { source: 'lead_results_preview', leadId: lead.id })
    } catch {
      setCopiedLeadShareLink(null)
    }
  }

  function openLeadShare(lead: PreviewLead): void {
    const base = typeof window !== 'undefined' ? window.location.origin : 'https://raelinfo.com'
    const shareUrl = `${base}/public/lead-preview/${encodeURIComponent(lead.id)}`
    const text = encodeURIComponent(`High-intent lead found: ${lead.company} (${lead.score}/100). Generated by raelinfo.com`)
    const linkedinUrl = `https://www.linkedin.com/sharing/share-offsite/?url=${encodeURIComponent(shareUrl)}`
    const twitterUrl = `https://twitter.com/intent/tweet?text=${text}&url=${encodeURIComponent(shareUrl)}`
    window.open(linkedinUrl, '_blank', 'noopener,noreferrer')
    window.open(twitterUrl, '_blank', 'noopener,noreferrer')
    track('lead_preview_shared_social', { source: 'lead_results_preview', leadId: lead.id })
  }

  return (
    <div className="space-y-6">
      <header className="space-y-3">
        <div className="flex flex-wrap gap-2">
          <Badge variant="outline" className="border-cyan-500/20 bg-cyan-500/10 text-cyan-200">
            Leads refresh daily
          </Badge>
          <Badge variant="outline" className="border-amber-500/30 bg-amber-500/10 text-amber-200">
            Limited preview
          </Badge>
        </div>
        <h1 className="text-2xl font-bold bloomberg-font neon-cyan">Lead Results</h1>
        <p className="text-sm text-muted-foreground max-w-3xl">
          Preview your best-fit leads for <span className="text-foreground font-medium">{company}</span>. You can copy AI outreach now, then unlock full lead tracking and campaign execution.
        </p>
        <div className="flex flex-wrap gap-2">
          <Button type="button" size="sm" variant="outline" onClick={() => void copyShareLink()}>
            {copiedShareLink ? 'Share link copied' : 'Copy share link'}
          </Button>
          <Button asChild size="sm" variant="outline">
            <Link href="/demo">
              Try RaelInfo demo
            </Link>
          </Button>
        </div>
      </header>

      <Card className="border-cyan-500/20 bg-card/50">
        <CardHeader className="pb-3">
          <CardTitle className="text-base">Step progress</CardTitle>
          <CardDescription>From search to outreach to campaign execution.</CardDescription>
        </CardHeader>
        <CardContent className="grid grid-cols-1 md:grid-cols-3 gap-3 text-sm">
          <div className="rounded border border-cyan-500/20 bg-cyan-500/10 p-3">
            <div className="text-xs uppercase tracking-wide text-cyan-200">Step 1</div>
            <div className="mt-1 font-medium text-foreground">Find leads</div>
            <div className="text-xs text-muted-foreground">Completed in demo search</div>
          </div>
          <div className="rounded border border-cyan-500/25 bg-cyan-500/10 p-3">
            <div className="text-xs uppercase tracking-wide text-cyan-200">Step 2</div>
            <div className="mt-1 font-medium text-foreground">Review matches + outreach</div>
            <div className="text-xs text-muted-foreground">Previewing top {FREE_PREVIEW_LIMIT} leads</div>
          </div>
          <div className="rounded border border-border bg-background/40 p-3">
            <div className="text-xs uppercase tracking-wide text-muted-foreground">Step 3 of 3</div>
            <div className="mt-1 font-medium text-foreground">Unlock full workflow</div>
            <div className="text-xs text-muted-foreground">Save leads, campaigns, and tracking</div>
          </div>
        </CardContent>
      </Card>

      <div className="grid grid-cols-1 gap-4">
        {visibleLeads.map((lead, index) => (
          <Card key={lead.id} className="border-cyan-500/20 bg-card/60">
            <CardHeader className="pb-3">
              <div className="flex flex-wrap items-start justify-between gap-3">
                <div>
                  <CardTitle className="text-lg">{lead.company}</CardTitle>
                  <CardDescription className="mt-1">
                    Lead score <span className="text-foreground font-medium">{lead.score}/100</span> · Preview lead {index + 1} of {FREE_PREVIEW_LIMIT}
                  </CardDescription>
                </div>
                <Badge className="bg-cyan-500/10 text-cyan-200 border-cyan-500/20">High-fit preview</Badge>
              </div>
            </CardHeader>
            <CardContent className="space-y-4">
              <div className="rounded border border-cyan-500/10 bg-background/40 p-3">
                <div className="text-xs uppercase tracking-wide text-muted-foreground">Why this lead is a good fit</div>
                <p className="mt-2 text-sm text-foreground">{lead.fitReason}</p>
                <p className="mt-2 text-xs text-muted-foreground">{lead.whyNow}</p>
                <div className="mt-2 text-xs text-muted-foreground">{lead.updatedAtLabel}</div>
                <div className="mt-3 text-xs uppercase tracking-wide text-muted-foreground">Based on</div>
                <ul className="mt-1 list-disc pl-5 text-xs text-muted-foreground">
                  {lead.scoreFactors.slice(0, 3).map((factor) => (
                    <li key={factor}>{factor}</li>
                  ))}
                </ul>
              </div>

              <div className="rounded border border-cyan-500/10 bg-background/40 p-3">
                <div className="text-xs uppercase tracking-wide text-muted-foreground">AI-generated outreach message</div>
                {lead.outreachSubject ? (
                  <div className="mt-2 text-sm text-foreground">
                    <span className="font-medium">Subject:</span> {lead.outreachSubject}
                  </div>
                ) : null}
                <p className="mt-2 text-sm whitespace-pre-wrap text-muted-foreground">{lead.outreachBody}</p>
              </div>

              <div className="flex flex-wrap gap-2">
                <Button
                  type="button"
                  variant="outline"
                  onClick={() => void copyOutreach(lead)}
                  className="neon-border hover:glow-effect"
                >
                  {copiedLeadId === lead.id ? (
                    <>
                      <Check className="h-4 w-4 mr-2 text-green-400" />
                      Copied
                    </>
                  ) : (
                    <>
                      <Copy className="h-4 w-4 mr-2" />
                      Copy
                    </>
                  )}
                </Button>
                <Button
                  type="button"
                  onClick={() => {
                    setUpgradePromptReason('campaign_action')
                    setShowUpgradeModal(true)
                    setShowInvitePromptLeadId(lead.id)
                    trackActivation('add_to_campaign')
                    track('demo_preview_add_to_campaign_clicked', { source: 'lead_results_preview', leadId: lead.id })
                  }}
                  className="neon-border hover:glow-effect"
                >
                  Add to Campaign
                </Button>
              </div>
              <div className="rounded border border-cyan-500/10 bg-background/40 p-3 space-y-2 text-xs text-muted-foreground">
                <div className="font-medium text-foreground">Share this result</div>
                <div>Generated by raelinfo.com</div>
                <div className="flex flex-wrap gap-2">
                  <Button type="button" size="sm" variant="outline" onClick={() => void copyLeadShareLink(lead)}>
                    {copiedLeadShareLink === lead.id ? 'Link copied' : 'Copy link'}
                  </Button>
                  <Button type="button" size="sm" variant="outline" onClick={() => openLeadShare(lead)}>
                    Share
                  </Button>
                </div>
              </div>
              {showInvitePromptLeadId === lead.id ? (
                <div className="rounded border border-purple-500/20 bg-purple-500/5 p-3 space-y-2 text-sm">
                  <div className="font-medium text-foreground">Invite your team</div>
                  <div className="text-xs text-muted-foreground">Bring teammates into this workflow and keep follow-up moving.</div>
                  <Button asChild size="sm" className="neon-border hover:glow-effect">
                    <Link href="/settings/team">Invite teammates</Link>
                  </Button>
                </div>
              ) : null}
            </CardContent>
          </Card>
        ))}
      </div>

      {hiddenLeadCount > 0 ? (
        <Card className="border-amber-500/30 bg-amber-500/5">
          <CardContent className="pt-6">
            <div className="flex flex-col sm:flex-row sm:items-center sm:justify-between gap-4">
              <div>
                <div className="text-xs uppercase tracking-wide text-amber-200">Step 3 of 3</div>
                <div className="mt-2 font-semibold text-foreground flex items-center gap-2">
                  <Lock className="h-4 w-4 text-amber-300" />
                  You&apos;ve unlocked 3 high-quality leads.
                </div>
                <p className="mt-1 text-sm text-foreground">These companies are actively growing and hiring.</p>
                <p className="mt-1 text-sm text-muted-foreground">
                  You&apos;ve already found qualified opportunities. Don&apos;t lose momentum - unlock 50+ more leads with no manual research.
                </p>
                <ul className="mt-3 space-y-1 text-sm text-muted-foreground">
                  <li>✔ 50+ similar leads</li>
                  <li>✔ Full contact data</li>
                  <li>✔ AI outreach sequences</li>
                  <li>✔ Daily new opportunities</li>
                </ul>
                <div className="mt-3 text-xs text-muted-foreground">Leads refresh daily • Cancel anytime</div>
              </div>
              <Button
                type="button"
                className="neon-border hover:glow-effect"
                onClick={() => {
                  setUpgradePromptReason('results_loaded')
                  setShowUpgradeModal(true)
                  track('demo_preview_paywall_opened', { source: 'lead_results_preview', hiddenLeadCount })
                }}
              >
                Unlock All Leads
              </Button>
            </div>
          </CardContent>
        </Card>
      ) : null}

      <Card className="border-cyan-500/20 bg-card/50">
        <CardHeader className="pb-3">
          <CardTitle className="text-base">Share this preview</CardTitle>
          <CardDescription>Share a public lead preview and invite others to try the workflow.</CardDescription>
        </CardHeader>
        <CardContent className="text-sm text-muted-foreground space-y-3">
          <div>
            Share this page to showcase lead quality and outreach drafts.
          </div>
          <div className="text-xs">CTA included: Try tool at /demo</div>
          <div className="flex flex-wrap gap-2">
            <Button type="button" size="sm" variant="outline" onClick={() => void copyShareLink()}>
              {copiedShareLink ? 'Copied' : 'Copy public preview link'}
            </Button>
            <Button asChild size="sm" className="neon-border hover:glow-effect">
              <Link href="/demo">Try tool</Link>
            </Button>
          </div>
        </CardContent>
      </Card>

      {showUpgradeModal ? (
        <div className="fixed inset-0 z-50 bg-black/60 backdrop-blur-sm flex items-center justify-center p-4">
          <Card className="w-full max-w-xl border-cyan-500/30 bg-card">
            <CardHeader>
              <CardTitle className="text-xl">Step 3 of 3</CardTitle>
              <CardDescription>Unlock all leads and keep your outbound momentum.</CardDescription>
            </CardHeader>
            <CardContent className="space-y-4">
              <div className="rounded border border-cyan-500/20 bg-cyan-500/10 p-3 text-sm text-foreground">
                <div className="font-semibold">You&apos;ve unlocked 3 high-quality leads.</div>
                <p className="mt-1 text-sm">These companies are actively growing and hiring.</p>
                <p className="mt-2 text-sm text-muted-foreground">
                  {upgradePromptReason === 'copy_action'
                    ? "You&apos;ve already copied outreach for a high-intent lead - don't lose momentum now."
                    : upgradePromptReason === 'campaign_action'
                      ? "You&apos;ve already found campaign-ready leads - don't lose momentum now."
                      : "You&apos;ve already found high-intent leads - don't lose momentum now."}{' '}
                  Unlock 50+ more leads with no manual research.
                </p>
                <ul className="mt-2 list-disc pl-5 text-muted-foreground space-y-1">
                  <li>✔ 50+ similar leads</li>
                  <li>✔ Full contact data</li>
                  <li>✔ AI outreach sequences</li>
                  <li>✔ Daily new opportunities</li>
                </ul>
              </div>
              <div className="text-xs text-muted-foreground">Leads refresh daily • Cancel anytime</div>
              <div className="flex flex-col sm:flex-row gap-2">
                <Button asChild className="neon-border hover:glow-effect">
                  <Link href="/pricing?target=closer">
                    <TrendingUp className="h-4 w-4 mr-2" />
                    Unlock All Leads
                  </Link>
                </Button>
                <Button asChild variant="outline">
                  <Link href={signupRedirect}>Create account</Link>
                </Button>
                <Button asChild variant="outline">
                  <Link href={referralLink}>Refer a teammate</Link>
                </Button>
                <Button asChild variant="ghost">
                  <Link href={`/login?mode=signin&redirect=${encodeURIComponent(`/lead-results?company=${encodeURIComponent(company)}`)}`}>
                    I already have an account
                  </Link>
                </Button>
              </div>
              <Button type="button" variant="ghost" onClick={() => setShowUpgradeModal(false)}>
                Close
              </Button>
            </CardContent>
          </Card>
        </div>
      ) : null}
    </div>
  )
}
